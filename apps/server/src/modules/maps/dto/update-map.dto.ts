import { IsBoolean, IsOptional, IsString, MaxLength } from 'class-validator';

export class UpdateMapDto {
  @IsOptional()
  @IsString()
  @MaxLength(100)
  title?: string;

  @IsOptional()
  @IsString()
  @MaxLength(500)
  description?: string;

  @IsOptional()
  @IsBoolean()
  isPublic?: boolean;
} 